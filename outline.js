/*

1) ==> Homepage (index.html)
What it usually contains:

Navigation bar- links to other pages like listings or login
Search section- where users can search for places (city, dates, guests)
Featured listings- a few highlighted properties
Categories or locations- popular places to stay
Footer- contact info, links, etc.

Its role in the website:index.html acts as the central hub that directs users to the rest of the site.

2) ==> Listings(listings.html):
Shows all available properties (houses/apartments) that users can browse.
Each property appears as a card with an image, title, location, and price, and users can click one to see more details.

3) ==> Log In Page (login.html):
Allows users to log into their account by entering their email/username and password.
 After logging in, users can manage bookings or make reservations.

4) ==> Property (property.html):
Displays the full details of a single property. 
This page includes photos, description, price per night, amenities, location, and a button to book or reserve the stay.

5) ==> main.css
This file contains the global styles for the entire website.
It sets the base appearance and layout used across all pages.

        Typical things inside main.css:
page font
background color
global spacing
layout rules
resets

6) ==> cards.css
This file styles property cards, which show listings like Airbnb.

       Each card usually contains:
property image
title
location
price
rating

7) ==> navbar.css
This file styles the navigation bar at the top of the website.

The navbar usually contains:
logo
links (Home, Listings, Login)
search bar
user menu

8) ==> listings.js
This file handles the property listings page.

        Typical things inside:
loading listing cards
displaying data
filtering or sorting properties
creating cards dynamically

9) ==> main.js
This is the main JavaScript file for general website behavior used on many pages.

         Typical things inside:
menu button behavior
dark/light interactions
shared buttons
general event listeners

10) ==> search.js
This file handles the search functionality.

         Typical things inside:
reading search input
filtering listings
checking location/date/guests
showing search results
     */